#include "libftprintf.h"

char	*ft_toupper(char *s, char *flag)
{
	int	i;

	i = 0;
	if (*flag == 'X')
	{
		while (s[i])
		{
			if (s[i] <= 122 && s[i] >= 97)
				s[i] -= 32;
			i++;
		}
	}
	return (s);
}
